#Ler dois valores inteiros e ver se o primeiro é maior que o segundo

primeiroValor = int(input('Digite o primeiro valor: '))
segundoValor = int(input('Digite o segundo valor: '))

if primeiroValor > segundoValor:
    print('Primeiro valor é maior que segundo valor')

#Ler um valor inteiro e ver se é par ou impar
valor = int(input('Digite um valor: '))

if valor % 2 == 0:
    print('Valor é par')
else:
    print('Valor é impar')

